package com.company.java;

public class operators {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        if (a && b){
            System.out.println("y");
        }
        else{
            System.out.println("n");
        }

        if (a || b){
            System.out.println("y");
        }
        else{
            System.out.println("n");
        }
    }
}
